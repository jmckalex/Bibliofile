/**
 * Plain, structured-clone-safe DTOs for the read-only viewer IPC contract.
 *
 * Everything here is a *value type*: plain objects, arrays, strings, numbers,
 * booleans, and `null`. NO class instances, NO functions, NO `Date` objects —
 * dates are ISO-8601 strings. This guarantees every payload survives Electron's
 * structured-clone IPC boundary unchanged.
 *
 * The renderer is intentionally "dumb": all display formatting (author lists,
 * de-TeXified titles, preview HTML) is done in the **main** process using the
 * core libraries (`@bibdesk/names`, `@bibdesk/tex`, `@bibdesk/model`). These DTOs
 * carry display-ready strings so the renderer just paints them.
 */

/** Opaque id for an open document. Generated by main; used to scope all reads. */
export type DocumentId = string;

/** Opaque stable id for a publication item within a document. */
export type ItemId = string;

// --- Open / close document --------------------------------------------------

/** A single non-fatal issue encountered while parsing a `.bib` file. */
export interface ParseWarning {
  /** `error` for recoverable parse problems; `warning` for advisories. */
  readonly severity: 'error' | 'warning';
  /** Human-readable message (display-ready). */
  readonly message: string;
  /** 1-based source line the issue relates to, if known. */
  readonly line?: number;
}

/** Request payload for opening a document. */
export interface OpenDocumentRequest {
  /** Absolute filesystem path to the `.bib` file. */
  readonly path: string;
}

/**
 * Response/notification payload describing a successfully opened document. Also
 * the body of the {@link IpcEvents.documentOpened} event.
 */
export interface OpenedDocument {
  /** Id to pass to every subsequent read request. */
  readonly documentId: DocumentId;
  /** Resolved absolute path of the opened file. */
  readonly path: string;
  /** Base filename for display (e.g. window title), without directory. */
  readonly displayName: string;
  /** Total number of publications parsed. */
  readonly itemCount: number;
  /** Non-fatal parse issues; empty array when the file parsed cleanly. */
  readonly warnings: readonly ParseWarning[];
}

/** Request payload for closing a document. */
export interface CloseDocumentRequest {
  readonly documentId: DocumentId;
}

/** Notification payload for {@link IpcEvents.documentClosed}. */
export interface ClosedDocument {
  readonly documentId: DocumentId;
}

// --- Open external ----------------------------------------------------------

/** Request to open a URL (browser) or local file (OS default app). */
export interface OpenExternalRequest {
  /** A URL (`http(s)://`, `doi:`/bare DOI handled by main) or a local file path. */
  readonly target: string;
  /** Whether `target` is a remote URL or a local file. */
  readonly kind: 'url' | 'file';
}

/** Result of an open-external request. */
export interface OpenExternalResult {
  readonly ok: boolean;
  /** Failure reason when `ok` is false. */
  readonly error?: string;
}

// --- Editing ----------------------------------------------------------------

/**
 * A single mutation applied to an open document. The renderer sends one of these
 * via `applyEdit`; main mutates the in-memory model and marks the doc dirty.
 * Field `value`s are raw BibTeX field text (not the de-TeXified display form).
 */
export type EditCommand =
  | { readonly kind: 'setField'; readonly itemId: ItemId; readonly field: string; readonly value: string }
  | { readonly kind: 'removeField'; readonly itemId: ItemId; readonly field: string }
  | { readonly kind: 'setCiteKey'; readonly itemId: ItemId; readonly citeKey: string }
  | { readonly kind: 'setType'; readonly itemId: ItemId; readonly entryType: string }
  | { readonly kind: 'generateCiteKey'; readonly itemId: ItemId }
  | { readonly kind: 'addEntry'; readonly entryType: string }
  | { readonly kind: 'duplicateEntry'; readonly itemId: ItemId }
  | { readonly kind: 'deleteEntry'; readonly itemId: ItemId }
  | { readonly kind: 'setMacro'; readonly name: string; readonly value: string }
  | { readonly kind: 'removeMacro'; readonly name: string };

/** Request to apply one {@link EditCommand} to a document. */
export interface ApplyEditRequest {
  readonly documentId: DocumentId;
  readonly command: EditCommand;
}

/** Outcome of an edit: the new dirty state and (when relevant) the affected item. */
export interface EditResult {
  readonly dirty: boolean;
  /** Item the edit focused on (for re-selection); absent for deletes/macros. */
  readonly affectedItemId?: ItemId;
  /** Refreshed detail of the affected item, when one applies. */
  readonly detail?: ItemDetail;
}

/** A document-level `@string` macro definition. */
export interface MacroDef {
  readonly name: string;
  /** Raw BibTeX value text (literal or `a # b` concatenation). */
  readonly value: string;
}

/** Request to list a document's `@string` macros. */
export interface ListMacrosRequest {
  readonly documentId: DocumentId;
}

/** A document's file-level macros, in dependency order. */
export interface ListMacrosResponse {
  readonly macros: readonly MacroDef[];
}

/** Request to save a document (optionally to a new path = Save As). */
export interface SaveDocumentRequest {
  readonly documentId: DocumentId;
  readonly targetPath?: string;
}

/** Result of a save: where it was written. */
export interface SaveDocumentResult {
  readonly documentId: DocumentId;
  readonly path: string;
}

