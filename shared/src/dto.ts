// --- List publications ------------------------------------------------------

/** Sort direction for a publications listing. */
export type SortDirection = 'asc' | 'desc';

/**
 * Sort spec for a publications listing. `key` is a {@link PublicationRow} column
 * key (or any BibTeX field name the main process knows how to sort by); main
 * does the actual sorting using the core comparators.
 */
export interface SortSpec {
  readonly key: string;
  readonly direction: SortDirection;
}

/** Request payload for a page/range of publications. */
export interface ListPublicationsRequest {
  readonly documentId: DocumentId;
  /** 0-based index of the first row to return. */
  readonly offset: number;
  /** Maximum number of rows to return. */
  readonly limit: number;
  /** Optional sort; main applies a default (cite key asc) when omitted. */
  readonly sort?: SortSpec;
  /** When present, restrict rows to members of this group (see {@link GroupNode}). */
  readonly groupId?: string;
}

/**
 * Thin row view-model for the virtualized publications table. Every field is a
 * display-ready string: `authorsDisplay` is already formatted (via the name
 * formatter) and `title` is already de-TeXified for display. The renderer never
 * re-formats these.
 */
export interface PublicationRow {
  /** Stable item id (row key + the id to pass to {@link GetItemDetailRequest}). */
  readonly id: ItemId;
  /** Cite key column. */
  readonly citeKey: string;
  /** BibTeX entry type, lowercased (e.g. `article`, `book`). */
  readonly type: string;
  /** Authors column — pre-formatted display string (may be empty). */
  readonly authorsDisplay: string;
  /** Title column — de-TeXified for display (may be empty). */
  readonly title: string;
  /** Year column — display string (may be empty when absent/unparseable). */
  readonly year: string;
}

/** Response payload for a publications listing. */
export interface ListPublicationsResponse {
  /** The requested window of rows. */
  readonly rows: readonly PublicationRow[];
  /** Total number of rows in the (filtered) result set, for the virtualizer. */
  readonly total: number;
}

// --- List groups ------------------------------------------------------------

/**
 * Kind of group, mirroring BibDesk's taxonomy (subsystem-04). `library` is the
 * whole library; `static`/`smart` are user groups; `category`/`author` are
 * auto-generated field groups (author = person category field); `url`/`script`
 * are external groups. Read-only viewer renders all of them; only membership is
 * exercised.
 */
export type GroupKind =
  | 'library'
  | 'static'
  | 'smart'
  | 'category'
  | 'author'
  | 'url'
  | 'script';

/** Request payload for the groups sidebar tree. */
export interface ListGroupsRequest {
  readonly documentId: DocumentId;
}

/**
 * One node in the groups sidebar. The tree is reconstructed from `parentId`
 * (root parents have no `parentId`); BibDesk's tree is at most two levels deep.
 */
export interface GroupNode {
  /** Stable group id (pass as {@link ListPublicationsRequest.groupId}). */
  readonly id: string;
  /** Group kind — drives the sidebar icon. */
  readonly kind: GroupKind;
  /** Display name (de-TeXified / formatted by main as appropriate). */
  readonly name: string;
  /** Number of publications in the group (`library` = whole library size). */
  readonly count: number;
  /** Parent group id; absent for the fixed root parent groups. */
  readonly parentId?: string;
}

/** Response payload for the groups sidebar — a flat list joined via `parentId`. */
export interface ListGroupsResponse {
  readonly groups: readonly GroupNode[];
}

// --- Item detail ------------------------------------------------------------

/** Request payload for one item's full detail. */
export interface GetItemDetailRequest {
  readonly documentId: DocumentId;
  readonly itemId: ItemId;
}

/** One field row in the detail view (display value, not raw BibTeX). */
export interface ItemField {
  /** Canonical field name (e.g. `Author`, `Title`, `Journal`). */
  readonly name: string;
  /** Display-ready value (macros expanded, de-TeXified by main). */
  readonly value: string;
  /** Raw BibTeX field text for editing (not de-TeXified / not brace-stripped). */
  readonly rawValue: string;
  /** True when the value is inherited from a crossref parent rather than set locally. */
  readonly isInherited: boolean;
}

/** One attachment (linked file or remote URL) in the detail view. */
export interface ItemFile {
  /** `file` for a local file, `url` for a remote URL. */
  readonly kind: 'file' | 'url';
  /** Display name (basename for files, label/host for URLs). */
  readonly displayName: string;
  /** Resolved URL string (`file://...`/absolute path for local files, absolute URL otherwise). */
  readonly url: string;
  /**
   * Source `Bdsk-File-N` field name when this attachment is a removable managed
   * file link. Absent for attachments synthesised from `Url`/`Local-Url` fields.
   */
  readonly field?: string;
}

/** Request to add attachment(s) to an item (main opens a file picker). */
export interface AddAttachmentRequest {
  readonly documentId: DocumentId;
  readonly itemId: ItemId;
}

/** Request to remove one managed attachment (`Bdsk-File-N`) from an item. */
export interface RemoveAttachmentRequest {
  readonly documentId: DocumentId;
  readonly itemId: ItemId;
  readonly field: string;
}

// --- Online search / import -------------------------------------------------

/** An online bibliographic source. */
export type OnlineSource = 'crossref' | 'arxiv';

/** The online sources the app offers. */
export const ONLINE_SOURCES: readonly { id: OnlineSource; label: string }[] = [
  { id: 'crossref', label: 'CrossRef' },
  { id: 'arxiv', label: 'arXiv' },
];

/** One normalised online search result (display fields + BibTeX fields to import). */
export interface OnlineResult {
  readonly source: OnlineSource;
  /** Suggested BibTeX entry type (e.g. `article`). */
  readonly entryType: string;
  readonly title: string;
  /** Display author list. */
  readonly authorsDisplay: string;
  readonly year: string;
  readonly venue?: string;
  readonly doi?: string;
  readonly url?: string;
  /** Canonical BibTeX fields used when importing this result as a new entry. */
  readonly fields: Readonly<Record<string, string>>;
}

/** Request to search an online source. */
export interface SearchOnlineRequest {
  readonly source: OnlineSource;
  readonly query: string;
}

/** Online search results (or an error message). */
export interface SearchOnlineResponse {
  readonly results: readonly OnlineResult[];
  readonly error?: string;
}

/** Request to import one online result into a document as a new entry. */
export interface ImportOnlineRequest {
  readonly documentId: DocumentId;
  readonly result: OnlineResult;
}

/**
 * Full detail of one publication for the detail/preview pane. `fields` are the
 * display field rows, `files` the attachments, and `previewHtml` an optional
 * pre-rendered HTML snippet for the preview card (rendered by main from a
 * template). The renderer treats `previewHtml` as trusted-but-sanitized content.
 */
export interface ItemDetail {
  readonly id: ItemId;
  readonly citeKey: string;
  /** BibTeX entry type, lowercased. */
  readonly type: string;
  readonly fields: readonly ItemField[];
  readonly files: readonly ItemFile[];
  /** Optional pre-rendered HTML for the preview card. */
  readonly previewHtml?: string;
  /** Raw markdown notes (the `Annote` field) for the notes editor. */
  readonly notesRaw: string;
  /** Rendered notes HTML (markdown + `[[citeKey]]` cross-references). */
  readonly notesHtml: string;
}
